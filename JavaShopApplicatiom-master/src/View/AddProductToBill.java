    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)productTable.getModel();
        int row = productTable.getSelectedRow();
        
        jtProductID.setText(model.getValueAt(row, 0).toString());
        jtPrice.setText(model.getValueAt(row, 2).toString());
        
    }//GEN-LAST:event_productTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel)productTable.getModel();
        
        int number = Integer.parseInt(jtNumber.getText());
        int productId = Integer.parseInt(jtProductID.getText());
        int price = Integer.parseInt(jtPrice.getText());
        int numberInStorage = Integer.parseInt(model.getValueAt(productTable.getSelectedRow(), 3).toString());
        
        if (number <= numberInStorage) {
            this.addNewBill.addNewProduct(productId,number,price);
            this.dispose();
        } else {
            this.dispose();
            System.out.print("So luong can < so luong ton kho");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

