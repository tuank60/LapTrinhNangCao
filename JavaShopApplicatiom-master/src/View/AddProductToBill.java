    public void loadData(){
        ArrayList<Product> products;
        try {
            products = productController.getAllProduct();
            DefaultTableModel model = (DefaultTableModel)productTable.getModel();
            Object[] row = new Object[5];
            for (int i = 0 ; i < products.size(); i++ ){
                row[0] = products.get(i).getId();
                row[1] = products.get(i).getName();
                row[2] = products.get(i).getPrice();
                row[3] = products.get(i).getNumber();
                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddProductToBill.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

