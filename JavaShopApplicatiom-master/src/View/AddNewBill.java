    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        int rowCount = table.getRowCount();
        int totalPrice = 0;
        for (int i =0; i < rowCount; i++ ) {
            int number = Integer.parseInt(model.getValueAt(i, 1).toString());
            int price = Integer.parseInt(model.getValueAt(i, 2).toString());
            totalPrice += number*price;
        }
        jlTotalPrice.setText(""+totalPrice);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int customerId = Integer.parseInt(jtCustomerID.getText());
        int userId = Integer.parseInt(jtUserId.getText());
        int totalPrice = Integer.parseInt(jlTotalPrice.getText());
        SimpleDateFormat dateFormat = new SimpleDateFormat ("hh:mm:ss dd.MM.yyyy");
        String time = dateFormat.format(new Date());
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        
        //check tat ca gia tri co du lieu
        addNewBillController.addNewBill(totalPrice, time, userId, customerId);
        int billId =addNewBillController.getBillId(time);
        
        
        for (int i = 0; i< table.getRowCount(); i++){
            int productId = Integer.parseInt(model.getValueAt(i, 0).toString());
            int price = Integer.parseInt(model.getValueAt(i, 2).toString());
            int number = Integer.parseInt(model.getValueAt(i, 1).toString());
            addNewBillController.addNewBillDetail(billId, number, price, productId);
            
            //update hang ton kho
            addNewBillController.updateStorage(productId, number);
            
        }
      
        
    }//GEN-LAST:event_jButton1ActionPerformed

