    public void addNewProduct(int productID,int number, int price){
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        // check xem mat hang da chon chua
        int rowCount = table.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            int table_productId = Integer.parseInt(model.getValueAt(i, 0).toString());
            if (productID == table_productId) {
                int newNumber = Integer.parseInt(model.getValueAt(i, 1).toString()) + number;
                if (addNewBillController.checkIfEnoughGood(productID, newNumber)== true) {
                    model.setValueAt(newNumber, i, 1);
                } else {
                    System.out.println("Ko du so luong hang ");
                }
                return;
            }  
        }
        // neu mat hang chua ton tai thi add row moi
        Object[] row = new Object[3];
        row[0] = productID;
        row[1] = number;
        row[2] = price;
        model.addRow(row);
        
    }    
    
