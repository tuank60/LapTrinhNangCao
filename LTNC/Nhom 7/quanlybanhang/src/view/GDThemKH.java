    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if((jTextField1.getText().length()==0)||(jTextField2.getText().length()==0)||(jDateChooser1.getDate()==null)||(jTextField4.getText().length()==0)){
            JOptionPane.showMessageDialog(null,"Ma khach hang, ten khach hang , ngay sinh va SDT khong duoc trong");
        }else{
            KhachHangController khc =new KhachHangController();
            try {
                //kiem tra xem max khach hang dien vao da ton tai trong CSDL hay chua
                if(khc.isExistedKhachhang(jTextField1.getText(),"")){
                    // thong bao da ton tai ma khach hang co ma nhu tren
                    JOptionPane.showMessageDialog(null,"Da ton tai khach hang voi ma khach hang tren"+jTextField1.getText());
                }else{
                    try {
                        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                        String dateString = format.format(jDateChooser1.getDate());
                        // Nguoc lai thi ta them moi khach hang
                        // neu bien success tra ve true thi nghia la them khach hang thanh cong
                        boolean success =khc.AddKhachhang(jTextField1.getText(), jTextField2.getText(),jDateChooser1.getDate(),jTextField4.getText(), jTextField5.getText(),jTextField6.getText());
                        if(success=true){
                            JOptionPane.showMessageDialog(rootPane, "Them khach hang thanh cong");    
                        }else{
                            JOptionPane.showMessageDialog(rootPane, "Thêm khach hang thất bại !");
                        }
                        this.dispose();
                        Home mf = new Home();
                        mf.setVisible(true);
                    } catch (HeadlessException | IOException | SQLException ex) {
                        Logger.getLogger(GDThemKH.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(GDThemKH.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(GDThemKH.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

