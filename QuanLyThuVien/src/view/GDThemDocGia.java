    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ((jTextField1.getText().length() == 0) || (jTextField2.getText().length() == 0) || (jDateChooser1.getDate() == null) || (jTextField5.getText().length() == 0)) {
            JOptionPane.showMessageDialog(null, "Mã độc giả, tên độc giả, ngày sinh và số điện thoại không được trống !");
        } else {
            DocGiaController dgc = new DocGiaController();
            // Kiểm tra xem mã độc giả điền vào đã tồn tại trong cơ sở dữ liệu hay chưa ?
            if (dgc.isExistedDG(Integer.parseInt(jTextField1.getText()))) {
                // Thông báo đã tồn tại độc giả có mã như trên
                JOptionPane.showMessageDialog(null, "Đã tồn tại độc giả với mã độc giả " + jTextField1.getText());
            } else {
                try {
                    SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                    String dateString = format.format(jDateChooser1.getDate());
                    // Ngược lại thì ta thêm mới độc giả
                    boolean success = dgc.addDocGia(Integer.parseInt(jTextField1.getText()), jTextField2.getText(), dateString, jTextField4.getText(), Integer.parseInt(jTextField5.getText()), jTextField6.getText());
                    // Nếu biến success trả về true thì nghĩa là thêm độc giả thành công
                    if (success == true) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm độc giả thành công !");
                        this.dispose();
                        MainFrame mf = new MainFrame();
                        mf.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Thêm độc giả thất bại !");
                        this.dispose();
                        MainFrame mf = new MainFrame();
                        mf.setVisible(true);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(GDThemDocGia.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

