    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Hãy chọn một độc giả !");
        } else {
            try {
                TTMTController ttmt = new TTMTController();
                boolean success = ttmt.addTTMT(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
                if (success) {
                    JOptionPane.showMessageDialog(null, "Thêm thông tin mượn trả thành công!");
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thông tin mượn trả thất bại!");
                }
             jComboBox1.setSelectedIndex(0);
             jComboBox2.setSelectedIndex(0);
             jTextArea1.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Lỗi : !" + e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

