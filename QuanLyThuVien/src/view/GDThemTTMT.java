    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        jComboBox1.setSelectedIndex(jComboBox2.getSelectedIndex());
        if (jComboBox1.getSelectedIndex() != 0) {
            for (DocGia dg : listDocGia) {
                if (dg.getMaDocGia() == Integer.parseInt(jComboBox1.getSelectedItem().toString())) {
                    jTextArea1.setText("Mã độc giả: " + dg.getMaDocGia() + "\nTên độc giả: " + dg.getTenDocGia() + "\nNgày sinh: " + dg.getNgaySinh() + "\nLớp học: " + dg.getLop() + "\nQuê quán:" + dg.getQueQuan() + "\nĐiện thoại: " + dg.getSoDienThoai());
                }
            }
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

